public class ControlFlow {
    public static void main(String[] args) {
        int a = 10, b = 20;
        if (a > b) {
            System.out.println("A is greater than B");
        } else if (a == b)
            System.out.println("Both A & B are equal");
        else
            System.out.println("B is Greater than A");
    }

}